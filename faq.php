<p class="faq_q">Q1. 如果在訂單處理期間，我可以變更我的訂單運送地址嗎？</p>
<p class="faq_a">A：HotPrint Cloud為客製化訂製服務，因此一般會需要5～7個工作天處理您的訂單，在處理過程中，您可使用e-mail聯繫我們為您更改運送或帳單地址，信中請留意需註明您的訂單號碼以利做更動的作業。但如果您訂製的貨品已經在運送作業中，恕無法更改運送地址，請見諒！</p>
<p>&nbsp;</p><p>&nbsp;</p>

<p class="faq_q">Q2. 我有促銷折扣，請問要如何使用？</p>
<p class="faq_a">A：當您進入結帳頁面時，會出現折扣碼的欄位，輸入促銷折扣碼後按下確認資料即可，促銷折扣碼為16碼英數所構成，請留意不要輸入錯誤囉！</p>
<p>&nbsp;</p><p>&nbsp;</p>

<p class="faq_q">Q3. 其他人可以印製我的圖片內容嗎？</p>
<p class="faq_a">A：HotPrint Cloud注重您個人的圖片隱私，只有您可以印製您所設計的圖片內容，除非您所選取的手機殼是HotPrint Cloud團隊所設計的版本。</p>
<p>&nbsp;</p><p>&nbsp;</p>

<p class="faq_q">Q4. HotPrint Cloud運送方式為何呢？</p>
<p class="faq_a">A：我們提供免費標準郵局送貨的方式處理您的訂單，包括所有國際的訂單，也會使用標準的EMS服務來提供運送服務。</p>
<p>&nbsp;</p><p>&nbsp;</p>

<p class="faq_q">Q5. HotPrint Cloud運送過程需要多長時間？</p>
<p class="faq_a">A：從您下訂單的當天開始，扣除5個工作日的製作時間，需要5-7個工作日為EMS（免費）運輸服務的時間。有些目的地可能需要更長的時間才能到貨，敬請耐心等候。</p>
<p class="faq_a"><strong>[貼心提醒您]</strong> 由於HotPrint Cloud對品質的要求，有時候訂單延遲可能會超過7各工作天進行製作，因為HotPrint Cloud團隊不容許有偷工減料或瑕疵的貨品交到您的手中。如果有任何延誤先跟您抱歉，並感謝您的耐心等候。</p>
<p>&nbsp;</p><p>&nbsp;</p>

<p class="faq_q">Q6. 我的包裹會被放到我的房子外面地上或需要自行到郵局領取嗎？</p>
<p class="faq_a">A：您專屬訂製的HotPrint Cloud貨品會送達您所指定的地址並讓您簽收以確認順利交到你的手上。</p>
<p>&nbsp;</p><p>&nbsp;</p>

<p class="faq_q">Q7. 如何查看我的訂單狀態？</p>
<p class="faq_a">A：您可以登錄到您的帳戶，點擊訂單歷史記錄進行查看。</p>
<p>&nbsp;</p><p>&nbsp;</p>

<p class="faq_q">Q8. 我如何付款購買HotPrint Cloud專屬訂製的手機殼？</p>
<p class="faq_a">A：HotPrint Cloud目前提供信用卡支付方式來處理您的訂單。</p>
